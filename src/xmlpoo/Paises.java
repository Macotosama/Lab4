    private void jButtonPrivinciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPrivinciaActionPerformed
        String provincia = jTextFieldProvincia.getText();
        if (!provincia.equals("")){
            provincias.add(provincia);
            provincia = jTextAreaProvincia.getText() + provincia + "\n";
            jTextAreaProvincia.setText(provincia);
            jTextFieldProvincia.setText("");
        }
    }//GEN-LAST:event_jButtonPrivinciaActionPerformed

