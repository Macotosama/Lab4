    private boolean isNum(String num){
	try {
		Integer.parseInt(num);
		return true;
	} catch (NumberFormatException nfe){
		return false;
	}
    }
    
    private boolean validarInfo() {
        if (jTextFieldNombre.getText().equals("")){
            return false;
        } else if (!isNum(jTextFieldPoblacion.getText())){
            return false;
        } else if (!isNum(jTextFieldTamanio.getText())){
            return false;
        } else if (jTextFieldIdioma.getText().equals("")) {
            return false;
        } else if (jTextAreaProvincia.getText().equals("")) {
            return false;
        } else if (jTextAreaPaisesLimitrofes.getText().equals("")) {
            return false;
        } else {
            return true;
        }
    }
    
