    private void jButtonAgregarPaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarPaisActionPerformed
        if (validarInfo()) {
            archivo.agregarPaiz();
            archivo.agregarCaracteristica("Nombre", jTextFieldNombre.getText());
            archivo.agregarCaracteristica("Tamaño", jTextFieldTamanio.getText());
            archivo.agregarCaracteristica("Población", jTextFieldPoblacion.getText());
            archivo.agregarCaracteristica("Idioma", jTextFieldIdioma.getText());
            for (int i = 0; i < provincias.size(); i++) {
                archivo.agregarCaracteristica("Provincia", provincias.get(i));
            }
            for (int i = 0; i < paisesLimitrofes.size(); i++) {
                archivo.agregarCaracteristica("Frontera", paisesLimitrofes.get(i));
            }
            javax.swing.JOptionPane.showMessageDialog(null, "País agregado");
            jTextFieldNombre.setText("");
            jTextFieldTamanio.setText("");
            jTextFieldPoblacion.setText("");
            jTextFieldIdioma.setText("");
            jTextAreaPaisesLimitrofes.setText("");
            jTextAreaProvincia.setText("");
            paisesLimitrofes.clear();
            provincias.clear();
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Error con los datos ingresados");
        }
    }//GEN-LAST:event_jButtonAgregarPaisActionPerformed

